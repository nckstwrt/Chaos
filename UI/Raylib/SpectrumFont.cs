    public void Init()
    {
        // Build atlas image: 16 chars wide × 6 rows = 128×48 px
        int atlasW = CharsPerRow * CharW;
        int atlasH = ((NumChars + CharsPerRow - 1) / CharsPerRow) * CharH;
        var img = Raylib.GenImageColor(atlasW, atlasH, Color.Blank);

        for (int ch = 0; ch < NumChars; ch++)
        {
            int col = ch % CharsPerRow;
            int row = ch / CharsPerRow;
            for (int y = 0; y < CharH; y++)
            {
                byte fontByte = ROM[ch * 8 + y];
                for (int x = 0; x < CharW; x++)
                {
                    if ((fontByte & (0x80 >> x)) != 0)
                    {
                        Raylib.ImageDrawPixel(ref img,
                            col * CharW + x, row * CharH + y, Color.White);
                    }
                }
            }
        }

        _atlas = Raylib.LoadTextureFromImage(img);
        Raylib.SetTextureFilter(_atlas, TextureFilter.Point);
        Raylib.UnloadImage(img);
    }

    public void DrawChar(char ch, int x, int y, Color colour)
    {
        int index = ch - 32;
        if (index < 0 || index >= NumChars) return;
        int col = index % CharsPerRow;
        int row = index / CharsPerRow;

        var src = new Rectangle(col * CharW, row * CharH, CharW, CharH);
        var dst = new Vector2(x, y);
        Raylib.DrawTextureRec(_atlas, src, dst, colour);
    }

    public void DrawString(string text, int x, int y, Color colour)
    {
        for (int i = 0; i < text.Length; i++)
            DrawChar(text[i], x + i * CharW, y, colour);
    }

    public void Unload() => Raylib.UnloadTexture(_atlas);
}
